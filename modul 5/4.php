$judul_buku = "Dasar-Dasar Web";
var_dump($judul_buku); // Menampilkan tipe dan nilai string

// Tipe data Integer
$harga = 95000;
var_dump($harga); // Menampilkan tipe dan nilai integer

// Tipe data Float (Double)
$rating = 4.5;
var_dump($rating); // Menampilkan tipe dan nilai float

// Tipe data Boolean
$tersedia = true;
var_dump($tersedia); // Menampilkan tipe dan nilai boolean

// Tipe data Array
$kategori = array("Web", "Pemrograman", "Desain");
var_dump($kategori); // Menampilkan tipe dan isi array

// Tipe data NULL
$pengarang = null;
var_dump($pengarang); // Menampilkan bahwa nilainya NULL
?>
